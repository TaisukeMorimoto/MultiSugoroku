package entity;

public class Result {

	private int id;
	private String name;
	private String date;
	private int score;
	private String select_character;
	private int rank;

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	public String getSelect_character() {
		return select_character;
	}
	public void setSelect_character(String select_character) {
		this.select_character = select_character;
	}
	public int getRank() {
		return rank;
	}
	public void setRank(int rank) {
		this.rank = rank;
	}

}
